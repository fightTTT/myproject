void InitDirectedBullet16_3(
	float& vx, float& vy,  // 弾の速度
	float theta            // 発射角度
) {
	// 速さ3に対する速度のテーブル
	static int v3[][2]={
		{ 3, 0}, { 3, 1}, { 2, 2}, { 1, 3},
		{ 0, 3}, {-1, 3}, {-2, 2}, {-3, 1},
		{-3, 0}, {-3,-1}, {-2,-2}, {-1,-3},
		{ 0,-3}, { 1,-3}, { 2,-2}, { 3,-1}
	};

	// 角度theta（0°〜360°）を16方向（0〜15）に変換する
	int dir=theta*16/360;

	// 弾の速度(vx,vy)をテーブルから求める
	vx=v3[dir][0];
	vy=v3[dir][1];
}

